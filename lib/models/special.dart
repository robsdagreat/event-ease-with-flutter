class Special {
  final String id;
  final String title;
  final String venue;
  final String description;
  final String imageUrl;
  final DateTime validUntil;
  final String discount;

  Special({
    required this.id,
    required this.title,
    required this.venue,
    required this.description,
    required this.imageUrl,
    required this.validUntil,
    required this.discount,
  });
}